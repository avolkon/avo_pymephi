# Напишите программу, которая запрашивает у пользователя температуру в градусах Цельсия
# и преобразует ее в градусы Фаренгейта и Кельвина с помощью функций to_fahrenheit и to_kelvin.
# Выведите результаты с точностью до двух знаков после запятой

# Запрашиваем у пользователя температуру в градусах Цельсия
celsius = float(input('Введите температуру в градусах Цельсия: '))

# Функция для преобразования в Фаренгейт
def to_fahrenheit(celsius):
    return celsius * 9/5 + 32

# Функция для преобразования в Кельвин
def to_kelvin(celsius):
    return celsius + 273.15

# Выполняем преобразование
fahrenheit = to_fahrenheit(celsius)
kelvin = to_kelvin(celsius)

# Выводим результаты с точностью до двух знаков
print(f"Температура в градусах Фаренгейта: {fahrenheit:.2f}")
print(f"Температура в градусах Кельвина: {kelvin:.2f}")
