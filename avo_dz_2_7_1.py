# #Вы аналитик в стартапе, и вам нужно прогнозировать количество пользователей вашего продукта в следующие несколько месяцев, предполагая, что каждый месяц количество пользователей увеличивается на определенный процент.
# Вам нужно определить, через сколько месяцев количество пользователей превысит определенный порог.
# Напишите функцию calculate_months_to_threshold(start, rate, threshold) для реализации данного подхода, где:
# start — количество пользователей в первый месяц (начальное значение)
# rate — темп ежемесячного роста в процентах
# threshold — значение, которое необходимо превысить
# Входные данные:
# start: int
# rate: int или float
# threshold: int
# Выходные данные:
# int: количество месяцев, необходимых, чтобы превысить threshold
# Примеры использования:
# calculate_months_to_threshold(1000, 10, 2000) → 8
# calculate_months_to_threshold(1797, 11, 9161) → 16
# calculate_months_to_threshold(100, 50, 500) → 4
# Исключения и особые случаи:
# - Если start >= threshold, результат должен быть 0 — цель уже достигнута
# - Если rate <= 0, выбрасывается ValueError: "Growth rate must be greater than 0."
# - Если start <= 0 или threshold <= 0, выбрасывается ValueError: "Start and threshold must be positive numbers."
# Ограничения:
# - Все входные значения должны быть положительными числами
# - rate может быть дробным (например, 2.5, 0.1), но не нулём и не отрицательным

def calculate_months_to_threshold():
    while True:
        try:
            start = int(input("Введите начальное количество пользователей (целое число): "))
            rate = float(input("Введите темп роста в процентах (десятичная дробь, ввод через '.'): "))
            threshold = int(input("Введите пороговое значение (целое число): "))

        except ValueError:
            print('Ошибка ввода, пожалуйста, введите число правильного типа:')
            continue

        if rate <= 0:
            print("Динамика роста не может быть нулевой или отрицательной, введите положительное число: ")
            continue

        if start >= threshold:
            print("Целевой показатель достигнут, поставьте новую цель.")
            continue

        break  # Все условия удовлетворены, выходим из цикла ввода

    max_iterations = 30  # Защита от бесконечного цикла
    month = 0

    while start < threshold and month < max_iterations:
        start += start * (rate / 100)
        month += 1

    if month == max_iterations:
        print("Достигнут лимит итераций. Возможно, условие не может быть выполнено.")
    else:
        print(f"Целевое значение количества пользователей {int(start)} , будет достигнуто через {month} месяцев")
calculate_months_to_threshold()